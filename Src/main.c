void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	static bool toggleFlag = false;
	static int count = 24;
	if (debounceSwitch() == true){
		led_Write(LED_BLUE|LED_GREEN|LED_ORANGE|LED_RED, LOW);
		toggleFlag ^= 1;
	}
	 if (toggleFlag== true){
		count++;
		if (count==25)
			{
			led_Toggle(LED_BLUE|LED_GREEN|LED_ORANGE|LED_RED);
				count = 0;
			}

	}
}
